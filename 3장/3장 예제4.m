% [1] 1차원 벡터 필터링
v = randn(1,20); % 평균 0, 표준편차 1의 난수 20개
idx_pos = v > 0; % 양수인 위치 논리 인덱스
pos_vals = v(idx_pos); % 양수만 추출

disp('원본 v:'); disp(v)
disp('양수만 추출:'); disp(pos_vals)

% [2] 2차원 행렬 필터링
M = randi([0,100], 10, 2); % 각 행: [온도, 압력] 데이터 샘플 10개
cond = (M(:,1) > 50) & (M(:,2) < 60); % 온도 > 50 && 압력 < 60 조건
filtered = M(cond, :);

disp('원본 M:'); disp(M)
disp('필터링된 M (온도>50 & 압력<60):'); disp(filtered)

% [3] 복수 조건 OR 사용
cond2 = (M(:,1) < 20) | (M(:,2) > 80);

% 첫 번째 열 20 미만, 두 번째 열 80 이상
disp('온도<20 또는 압력>80:'); disp(M(cond2, :))